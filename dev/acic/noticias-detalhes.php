                <p>Foram iniciadas nesta terça-feira, 29, as obras da nova emergência do Hospital Maicé de Caçador. A engenheira civil Jane Correa Darela, especialista em obras hospitalares informa que foram iniciadas as obras transferindo o acesso das ambulâncias e acesso de emergência para a porta central, que dividirá a recepção com o setor de imagem do Hospital.
“Estamos reformulando a parte interna para continuar tendo os mesmos serviços que temos atualmente.  Vamos primeiramente fazer a ampliação da emergência, uma obra de 380 metros quadrados. Posteriormente trazemos os serviços para essa nova área e começamos a reforma interna até ir finalizando com a nova recepção”, explica. A obra tem previsão de 360 dias.
                </p>
                
                <p>
O presidente do Conselho Consultivo Leonir Tesser afirma que o Hospital caminha para uma nova realidade. “Estamos muito felizes com tudo que está acontecendo. A comunidade abraçou o Hospital e o resultado está aparecendo. Um novo Maicé está surgindo para beneficiar toda a comunidade”, salienta.
                </p>
                
                <p>
Henrique Basso, presidente da ACIC destaca que as obras que estão acontecendo hoje no Hospital eram um sonho distante até bem pouco tempo atrás. Mas a comunidade uniu esforços e através de diversas ações, as obras estão saindo. “Estamos trabalhando com ações, campanhas e recebendo a ajuda de várias formas. São doações em dinheiro, mas também em serviços, em equipamentos ou em gestos que emocionam. Tenho certeza que todo este esforço vai valer a pena”, reforça.
</p>
                
